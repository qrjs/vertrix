//==============================================================================
// 单端口SRAM (Single-Port SRAM)
//==============================================================================
// 功能特性:
//   - 8位数据宽度
//   - 可配置深度和地址宽度
//   - 同步读写（支持同时读写）
//   - 用于存储权重(W)和输入(I)数据
//
// 用途:
//   - 存储矩阵A (8×8 INT8)
//   - 存储矩阵B (8×8 INT8)
//==============================================================================

module sram #(
    parameter DEPTH      = 64,        // 存储深度（默认8×8=64）
    parameter ADDR_WIDTH = 6,         // 地址宽度（log2(64)=6）
    parameter DATA_WIDTH = 8,         // 数据宽度（INT8）
    parameter INIT_VAL   = 0          // 初始值
)(
    // 端口定义
    input  wire                    clk,
    input  wire                    wen,            // 写使能
    input  wire                    ren,            // 读使能
    input  wire [ADDR_WIDTH-1:0]   addr,           // 读写地址
    input  wire [DATA_WIDTH-1:0]   din,            // 写数据
    output reg  [DATA_WIDTH-1:0]   dout            // 读数据
);

    //==========================================================================
    // 内部存储器数组
    //==========================================================================
    reg [DATA_WIDTH-1:0] mem [0:DEPTH-1];

    //==========================================================================
    // 初始化（可选）
    //==========================================================================
    integer i;
    initial begin
        for (i = 0; i < DEPTH; i = i + 1) begin
            mem[i] = INIT_VAL;
        end
    end

    //==========================================================================
    // 写操作
    //==========================================================================
    always @(posedge clk) begin
        if (wen) begin
            mem[addr] <= din;
        end
    end

    //==========================================================================
    // 读操作
    //==========================================================================
    always @(posedge clk) begin
        if (ren) begin
            dout <= mem[addr];
        end
    end

    //==========================================================================
    // 断言：检查地址范围
    //==========================================================================
    `ifdef FORMAL
        always @(posedge clk) begin
            assert (addr < DEPTH) else $error("Address out of range: %0d >= %0d", addr, DEPTH);
        end
    `endif

endmodule
